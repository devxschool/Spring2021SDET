package loops;

public class ForLoop {

    // For loop is another type of loops in java. The difference of this loop is that we should know the number
    // of times that loop should iterate.

    public static void main(String[] args) {


        for (int i = 0; i < 1; i++){

            System.out.println(i + ": Hello world!"); // is the body of the loop

        }

    }
}
